// 1. Arithmetic operators

let x = 10;
let y = 3;
let a = 5;
let b = 7;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
console.log(x ** y);

// additionally we have two operators

// increment (++)
console.log(++x); // 11
console.log(y++); // 3

// decrement (--)
console.log(--a); // 4
console.log(b--); // 7